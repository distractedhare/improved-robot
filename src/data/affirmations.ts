/** Return a random affirmation, optionally excluding a previous one to avoid repeats. */
export function getRandomAffirmation(exclude?: string): string {
  const pool = exclude ? AFFIRMATIONS.filter(a => a !== exclude) : AFFIRMATIONS;
  return pool[Math.floor(Math.random() * pool.length)];
}
