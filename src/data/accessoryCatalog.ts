// ── Lookup helpers ──

export const CATALOG_BY_ID: Map<string, CatalogItem> = new Map(
  CATALOG.map(item => [item.id, item])
);

export function getCatalogByCategory(category: CatalogItem['category']): CatalogItem[] {
  return CATALOG.filter(item => item.category === category);
}

export function getItemById(id: string): CatalogItem | undefined {
  return CATALOG_BY_ID.get(id);
}

export function getItemsByIds(ids: string[]): CatalogItem[] {
  return ids.map(id => CATALOG_BY_ID.get(id)).filter((item): item is CatalogItem => item !== undefined);
}
